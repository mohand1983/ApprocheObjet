package entites2;

public class Personne {

		// les attributs 
	public String nom;
	public String prenom;
	public String AdressePostale;
		// le constructeur
	public Personne(String nom, String prenom, String adressePostale) {
		super();
		this.nom = nom;
		this.prenom = prenom;
		AdressePostale = adressePostale;
	}
	
	

}
